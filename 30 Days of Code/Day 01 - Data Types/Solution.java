import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

	public static void main(String[] args) {
		int i = 4;
		double d = 4.0;
		String s = "HackerRank ";
		Scanner scan = new Scanner(System.in);
		int iNew = scan.nextInt();
		double dNew = scan.nextDouble();
		scan.nextLine();
		String sNew = scan.nextLine();
		System.out.println(i + iNew);
		System.out.println(d + dNew);
		System.out.println(s + sNew);
		scan.close();
	}

}